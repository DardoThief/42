#include "libft.h"

void	ft_putnbr(int n)
{
	size_t nbr;

	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		ft_putnbr(147483648);
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
		nbr = n;
	}
	if (n > 0)
		nbr = n;
	if ((nbr / 10) > 0)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
