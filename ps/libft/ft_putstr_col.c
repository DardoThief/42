#include "libft.h"

void	ft_putstr_col(char *str, char col)
{
	if (col >= '0' && col <= '7')
	{
		ft_putstr("\033[3");
		ft_putchar(col);
		ft_putstr("m");
		ft_putstr(str);
		ft_putstr("\033[0m");
	}
}
