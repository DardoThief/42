#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int j;
	char *s1;
	char *s2;

	if (ac == 3)
	{
		s1 = av[1];
		s2 = av[2];
		i = 0;
		j = 0;
		while (s2[i])
		{
			if (s1[j] == s2[i])
				j++;
			i++;
		}
		if (s1[j] == 0)
			write(1, s1, j);
	}
	write(1, "\n", 1);
	return (0);
}
