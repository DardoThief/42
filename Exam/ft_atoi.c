int		ft_atoi(char *str)
{
	int nbr;
	int sign;

	sign = 1;
	nbr = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
		{
			sign = -1;
			str++;
		}
		else
			str++;
	}
	while (*str >= '0' && *str <= '9' && *str != '\0')
	{
		nbr = nbr * 10 + (*str - '0');
		str++;
	}
	nbr = nbr * sign;
	return (nbr);
}
